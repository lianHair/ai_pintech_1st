// 문자열(String) - "", '', ``(backtick)
const number = "1";
const number1 = `1`;
const number2 = "1";
console.log(number + number1); // = 11

// 숫자(Number)
const age = 100;
const age1 = 20;
console.log(age + age1); // = 120

// Number화 시키기
const a = Number("100"); // 100
const b = Number("200"); // 200
console.log(a + b); // = 300

//String화 시키기
const e = string(100); // "100"
const f = string(4); // "4"
console.log(e + f); // = 1004
